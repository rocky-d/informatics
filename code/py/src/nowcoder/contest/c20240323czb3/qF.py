from collections import deque
from math import *


def main() -> None:
    n = int(input())

    ans = deque([(4 * n + 3) * '*'])
    power = floor(log2(n))
    nums = []
    for i in range(power, -1, -1):
        tmp = []
        num = 2 ** i
        for x in range(num, n + 1, 2 * num):
            tmp.append(x)
        nums.append(tmp)
    print(*nums, sep = '\n')

    for i, ls in enumerate(nums):
        p = power - i
        gap = 2 ** p
        print(f"{gap=}")
        s = '*'
        if 1 < gap:
            s += '|' + (2 ** (p + 2) - 5) * ' '
        for num in ls:
            s += f"|{num:<3}" + '|' + (4 * gap - 1) * ' '
        ans.append(s + (4 * n + 2 - len(s)) * ' ' + '*')

    ans.append('*' + n * '+---' + '+*')
    s = '*'
    for num in range(1, 1 + n):
        s += f"|{num:<3}"
    ans.append(s + '|*')
    ans.append('*' + n * '+---' + '+*')
    ans.append((4 * n + 3) * '*')
    print(*ans, sep = '\n')


if __name__ == '__main__':
    main()

